package kr.ac.kopo.day0306;

public class HelloMain {

	public static void main(String[] args) {
		
		int a = 5;
		
		System.out.printf("%d %c %4d", 3,'+', 4); // %d=๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ, %f=๏ฟฝวผ๏ฟฝ, %c=๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ, %s=๏ฟฝ๏ฟฝ๏ฟฝฺฟ๏ฟฝ ,%b=๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
													//'%'วฅ๏ฟฝ๏ฟฝ๏ฟฝ printf()๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
													// %๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝฺด๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝฺธ๏ฟฝ๏ฟฝ๏ฟฝ
		System.out.println(a++); // a++ = print(a) ๏ฟฝฯฐ๏ฟฝ a = a+1
		System.out.println(++a); // ++a = a = a+1 ๏ฟฝฯฐ๏ฟฝ print(a)
	
	
	}
}
